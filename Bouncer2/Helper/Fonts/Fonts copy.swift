import UIKit

extension UIFont {
    
    class func poppinsLight(size: CGFloat) -> UIFont {
        return UIFont(name: "Poppins-Light", size: size)!
    }
    
    class func poppinsThin(size: CGFloat) -> UIFont {
        return UIFont(name: "Poppins-Thin", size: size)!
    }

    class func poppinsRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Poppins-Regular", size: size)!
    }

    class func poppinsMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Poppins-Medium", size: size)!
    }
    
    class func poppinsSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Poppins-SemiBold", size: size)!
    }
    
    class func poppinsMediumItalic(size: CGFloat) -> UIFont{
        return UIFont(name: "Poppins-MediumItalic", size: size)!
    }
    
    class func poppinsRegularItalic(size: CGFloat) -> UIFont{
        return UIFont(name: "Poppins-Italic", size: size)!
    }
}
